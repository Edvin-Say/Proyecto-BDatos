public class BalanceG {
    protected String bg; 
    protected String activos;

    public BalanceG(String bg, String activos) {
        this.bg = bg;
        this.activos = activos;
    }

    public String getBg() {
        return bg;
    }

    public void setBg(String bg) {
        this.bg = bg;
    }

    public String getActivos() {
        return activos;
    }

    public void setActivos(String activos) {
        this.activos = activos;
    }
    
    
    
    
    
    
    
}
